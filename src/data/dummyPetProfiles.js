// Function to load dummy profiles into localStorage
export const loadDummyProfiles = () => {
  const existingProfiles = JSON.parse(localStorage.getItem('petProfiles') || '[]');
  
  // Only add dummy profiles if no profiles exist
  if (existingProfiles.length === 0) {
    localStorage.setItem('petProfiles', JSON.stringify(dummyPetProfiles));
    console.log('Dummy pet profiles loaded successfully!');
    return dummyPetProfiles;
  }
  
  return existingProfiles;
};

// Function to reset to dummy profiles
export const resetToDummyProfiles = () => {
  localStorage.setItem('petProfiles', JSON.stringify(dummyPetProfiles));
  console.log('Reset to dummy pet profiles!');
  return dummyPetProfiles;
};

